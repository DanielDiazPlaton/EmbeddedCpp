#include <iostream>
#include <cstdint>

using namespace std;

int main(){

/*******************************************
 * &
 *******************************************/
    uint16_t x;        //0010
    uint16_t y;
    uint8_t mask = 1;  // 0001

    for(x = 0; x < 11; x++)
        cout << x << ": " << ((x & mask)? "Impar" : "Par") << endl;

/*******************************************
 * << >>
 *******************************************/
    for(y = 0; y < 11; y++)
        cout << (y << 1) << endl;




} // End main